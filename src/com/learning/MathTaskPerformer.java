package com.learning;
public class MathTaskPerformer {
    public double add(int a, int b) {
        double result = a + b;
        System.out.println(result);
        return result;
    }
    public double sub(int a, int b) {
        double result = a - b;
        System.out.println(result);
        return result;
    }
    public double div(int a, int b) {
        double result = a / b;
        System.out.println(result);
        return result;
    }
    public double mult(int a, int b) {
        double result = a * b;
        System.out.println(result);
        return result;
    }
}
